const getSuma = (val1, val2) => {
  return val1 + val2;
};

const getResta = (val1, val2) => {
  return val1 - val2;
};

const getMult = (val1, val2) => {
  return val1 * val2;
};

const getDiv = (val1, val2) => {
  return val1 / val2;
};

module.exports = {
  getSuma,
  getResta,
  getMult,
  getDiv,
};
